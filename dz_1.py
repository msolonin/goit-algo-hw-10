# -*- coding: utf-8 -*-
"""
Завдання 1. Оптимізація виробництва

Компанія виробляє два види напоїв: "Лимонад" і "Фруктовий сік". Для виробництва цих напоїв
використовуються різні інгредієнти та обмежена кількість обладнання. Задача полягає у максимізації виробництва,
враховуючи обмежені ресурси.

Умови завдання:

    "Лимонад" виготовляється з "Води", "Цукру" та "Лимонного соку".
    "Фруктовий сік" виготовляється з "Фруктового пюре" та "Води".
    Обмеження ресурсів: 100 од. "Води", 50 од. "Цукру", 30 од. "Лимонного соку" та 40 од. "Фруктового пюре".
    Виробництво одиниці "Лимонаду" вимагає 2 од. "Води", 1 од. "Цукру" та 1 од. "Лимонного соку".
    Виробництво одиниці "Фруктового соку" вимагає 2 од. "Фруктового пюре" та 1 од. "Води".

Використовуючи PuLP, створіть модель, яка визначає, скільки "Лимонаду" та "Фруктового соку" потрібно виробити
для максимізації загальної кількості продуктів, дотримуючись обмежень на ресурси. Напишіть програму, код якої
максимізує загальну кількість вироблених продуктів "Лимонад" та "Фруктовий сік", враховуючи обмеження на кількість
ресурсів.
"""
from pulp import LpMaximize, LpProblem, LpVariable


def main():
    model = LpProblem(name="maximize_production", sense=LpMaximize)
    lemonade = LpVariable(name="lemonade", lowBound=0, cat="Integer")
    fruit_juice = LpVariable(name="fruit_juice", lowBound=0, cat="Integer")
    model += 2 * lemonade + fruit_juice <= 100  # Вода
    model += lemonade <= 50  # Цукор
    model += lemonade <= 30  # Лимонний сік
    model += 2 * fruit_juice <= 40  # Фруктове пюре
    # Додавання функції максимізації
    model += lemonade + fruit_juice
    # Вирішення задачі
    model.solve()
    # Вивід результатів
    print(f"Сатус: {model.status}")
    print(f"Максимально вироблено лемонаду: {round(lemonade.value())}")
    print(f"Максимально вироблено соку: {round(fruit_juice.value())}")


if __name__ == '__main__':
    main()
